package hjow.methodconverter.ui;

import java.awt.Component;

/**
 * <p>Class object which is implements this has a GUI component.</p>
 * 
 * <p>이 인터페이스를 구현한 클래스는 GUI 컴포넌트를 가집니다.</p>
 * 
 * @author HJOW
 *
 */
public interface IsComponent
{
	/**
	 * <p>Return component object.</p>
	 * 
	 * <p>컴포넌트를 반환합니다.</p>
	 * 
	 * @return Component
	 */
	public Component getComponent();
}
